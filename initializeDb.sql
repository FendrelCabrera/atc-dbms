CREATE TABLE customer (
cid int IDENTITY(1, 1) PRIMARY KEY,
cname varchar(20) NOT NULL,
age int NOT NULL,
gender char(1) NOT NULL,
email varchar(40) NOT NULL UNIQUE,
pass varchar(15) NOT NULL
); 

CREATE TABLE runway (
runId int IDENTITY(1, 1) PRIMARY KEY,
runStatus bit NOT NULL,
length int NOT NULL
);

CREATE TABLE airline (
alCode varchar(3) PRIMARY KEY,
alName varchar(20) NOT NULL
);

CREATE TABLE airport (
aCode char(3) PRIMARY KEY,
aName varchar(70) NOT NULL,
loc varchar(50) NOT NULL
);

CREATE TABLE flight (
fNum char(6) PRIMARY KEY,
model varchar(20) NOT NULL,
capacity int NOT NULL,
alCode varchar(3) FOREIGN KEY REFERENCES airline(alCode) ON DELETE CASCADE,
);

CREATE TABLE route (
rid int IDENTITY(1, 1) PRIMARY KEY,
rtype bit NOT NULL,
dtime datetime NOT NULL,
fNum char(6) FOREIGN KEY REFERENCES flight(fNum) ON DELETE CASCADE,
runId int FOREIGN KEY REFERENCES runway(runId) ON DELETE CASCADE,
locId char(3) FOREIGN KEY REFERENCES airport(aCode) ON DELETE CASCADE
);

CREATE TABLE ticket(
tNo int IDENTITY(1, 1) PRIMARY KEY,
cid int FOREIGN KEY REFERENCES customer(cid) ON DELETE CASCADE,
rid int FOREIGN KEY REFERENCES route(rid) ON DELETE CASCADE,
seatNum char(3) NOT NULL,
class char(1) NOT NULL,
mealOption bit NOT NULL
);

